    private boolean fieldsRight() {
        //TODO verify that fiels are correctly filled
        if (!verifyLenght(nameField,100,"Name too long, please make it shorter")) {
            return false;
        }
        if (!isInt(quantField,"Zip code should be numbers")) {
            return false;
        }
        if (!isInt(priceField,"Zip code should be numbers")) {
            return false;
        }
        
        return true;
    }
    
    private boolean verifyLenght(JTextField text,int length,String message) {
        if (text.getText().length() > length) {
            
            JOptionPane.showMessageDialog(this, message,
                    "Warning", JOptionPane.WARNING_MESSAGE);
            text.requestFocus();
            text.selectAll();
            return false;
        } else {
            return true;
        }
    }
    
    private boolean isInt(JTextField text,String message) {
        String str = text.getText();
        if (!str.matches("^-?\\d+$")) {
            JOptionPane.showMessageDialog(this, message,
                    "Warning", JOptionPane.WARNING_MESSAGE);
            text.requestFocus();
            text.selectAll();
            return false;
        }
        return true;
    }
    
