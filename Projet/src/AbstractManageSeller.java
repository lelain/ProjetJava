    //Test if the name field is not already in the db, if yes display message in dialog
    protected boolean verifyName(JTextField text,String message) {
        String str = text.getText();
        str=str.replaceAll("'","\\\\'");    //in case there is apostrophe in the string, we replace ' by \' 
        Statement stmt=null;
        try{
            stmt = conn.createStatement();
            String sqlQuery;
            sqlQuery="select name from V_Sellers where name='"+str+"'";
            ResultSet rs = stmt.executeQuery(sqlQuery);
            if (rs.next()) { 
                //here we already have this name in the database --> impossible
                JOptionPane.showMessageDialog(this, message,
                    "Warning", JOptionPane.WARNING_MESSAGE);
                text.requestFocus();
                text.selectAll();
                return false;   
            } else {
                //here everything is ok
                return true;
            }
        } catch(SQLException se) {
            //Handle errors for JDBC
            JOptionPane.showMessageDialog(this, "Unexpected error, Request problem\nDetails : "+se.getMessage(),
                "Warning", JOptionPane.ERROR_MESSAGE);
            return false; 
        } finally {
            //finally block used to close resources
            try{
                if(stmt!=null)
                stmt.close();
            }catch(SQLException se2){ return false;  }// nothing we can do
        }//end finally
    }
    
    
    //Test if the fields are correctly filled
    protected boolean fieldsRight() {
        if (!Utilities.verifyLenght(jTextField1,40,"Name too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextField4,30,"Email adress too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextField2,20,"Phone number too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextField3,20,"Phone number too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextField5,20,"weixin number too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextField6,80,"Street too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextField7,30,"City too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextField8,8,"Zip code too long, please make it shorter",this)) {
            return false;
        }
        
        //if the zipcode textField is not empty we check if it is int
        if (!"".equals(jTextField9.getText())) {
            if (!Utilities.isInt(jTextField8,"Zip code should be numbers",this)) {
                return false;
            }
        }    
        if (!Utilities.verifyLenght(jTextField9,40,"Country too long, please make it shorter",this)) {
            return false;
        }
        if (!Utilities.verifyLenght(jTextArea1,500,"Information too long, please make it shorter",this)) {
            return false;
        }
        if (!verifyName(jTextField1,"Name already in the data base, please find a new one")) {
            return false;
        }

        return true;
    }
    
    
